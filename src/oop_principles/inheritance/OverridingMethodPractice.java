package oop_principles.inheritance;

public class OverridingMethodPractice {
    public static void main(String[] args) {

        Tester tester = new Tester();

        tester.eat(); // extended from the Person Class
        tester.sleep(); // extended from the Person Class
        tester.walk(); // extended from the Person Class
        tester.learn(); // extended from the Person Class
        tester.code(); // coming from Tester Class





    }
}
