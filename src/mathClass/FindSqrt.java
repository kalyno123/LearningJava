package mathClass;

public class FindSqrt {
    public static void main(String[] args) {

        int a = 36; // 6.0
        int b = 49; // 7.0
        int c = 16; // 4.0
        double d = 56.2; // 7.496665925596525

        System.out.println("a = " + Math.sqrt(a));
        System.out.println("b = " + Math.sqrt(b));
        System.out.println("c = " + Math.sqrt(c));
        System.out.println("d = " + Math.sqrt(d));


    }

}
