package variables;

public class MultipleDeclarationAndAssignment {
    public static void main(String[] args) {

//        LONG WAY OF DECLARING MULTIPLE ITEMS:
//        int myFirstTask;
//        int mySecondTask;

//        FASTER WAY OF DECLARING MULTIPLE ITEMS:
        int myFirstTask, mySecondTask;

//        LONG WAY OF DECLARING MULTIPLE ITEMS:
//        double myFDouble;
//        double mySDouble;
//        double myTDouble;

//        FASTER WAY OF DECLARING MULTIPLE ITEMS:
        double myFDouble, mySDouble,myTDouble;

//      LONG WAY OF DECLARING/ASSIGNING MULTIPLE ITEMS:
//      char myFirstChar = 'c';
//      char mySecondChar = 'y';
//      char myThirdChar;
//      char myFourthChar = 'z';

//      FASTER WAY OF DECLARING/ASSIGNING MULTIPLE ITEMS:
        char myFirstChar, mySecondChar, myThirdChar, myFourthChar;




    }
}
