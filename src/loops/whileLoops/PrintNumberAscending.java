package loops.whileLoops;

public class PrintNumberAscending {
    public static void main(String[] args) {

    /* PRACTICE TASK:
    print numbers from 1 to 7 (both included)
    */
        for (int i = 1; i <= 7; i++){
            System.out.println(i);
        }
        int i = 1;
        while (i <= 7){
            System.out.println(i);
            i++;
        }




    }
}
