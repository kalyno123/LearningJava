package loops.forLoops;

public class PrintNumbersAscending {
    public static void main(String[] args) {

    /* Write a Java program to print numbers ascending from 1 to 10 (1 and 10 are included)

    NOTE - FOR LOOPS SYNTAX:
    for(initialization; termination (condition for loops to run); change (increment/decrement value)){
            // BLOCK OF CODE TO EXECUTE
    }
    */

        for(int i = 1; i <= 10; i++){ // i = 1 -> start counting by 1; i <= 10 -> stop at 10th iteration; i++ -> count going up by 1 each time
            System.out.println(i);
        }






    }
}
